export function searchProducts(query: string): SearchProduct[] {
  if (!query.trim()) return MOCK_SEARCH_PRODUCTS
  const q = query.toLowerCase()
  return MOCK_SEARCH_PRODUCTS.filter(
    (p) =>
      p.title.toLowerCase().includes(q) ||
      (p.category?.toLowerCase().includes(q) ?? false) ||
      (p.material?.toLowerCase().includes(q) ?? false),
  )
}

export function getAutocompleteSuggestions(query: string): SearchProduct[] {
  if (!query.trim()) return []
  return searchProducts(query).slice(0, 5)
}
